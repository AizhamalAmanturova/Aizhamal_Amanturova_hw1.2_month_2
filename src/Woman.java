public class Woman {
    private int age;
    private String name;
    private Character character;

    public Woman(int age, String name, Character character) {
        this.age = age;
        this.name = name;
        this.character = character;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public Character getCharacter() {
        return character;
    }
}
